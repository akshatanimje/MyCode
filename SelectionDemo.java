
public class SelectionDemo
{

	public static void main(String[] args)
	{
		String s="rst";
		switch(s)
		{
		case "abc":
			System.out.println("one");
			break;
		case "pqr":
			System.out.println("two");
			break;
		default:
			System.out.println("no match");
		}
		
	
	}


}
